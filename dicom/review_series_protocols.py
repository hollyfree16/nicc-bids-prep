def read_tsv(path: Path) -> Tuple[List[dict], List[str]]:
    with path.open(newline="", encoding="utf-8", errors="replace") as f:
        reader = csv.DictReader(f, delimiter="\t")
        rows = list(reader)
        if reader.fieldnames is None:
            raise SystemExit(f"ERROR: {path} does not look like a TSV with a header")
        return rows, list(reader.fieldnames)


def write_tsv(path: Path, rows: Sequence[dict], fieldnames: Sequence[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, delimiter="\t")
        writer.writeheader()
        for row in rows:
            writer.writerow({k: row.get(k, "") for k in fieldnames})


def get(row: dict, key: str) -> str:
    return (row.get(key) or "").strip()


def as_int(value, default: int = 999999) -> int:
    try:
        return int(float(str(value)))
    except Exception:
        return default


def norm_text(value: str) -> str:
    value = (value or "").casefold()
    value = re.sub(r"[_\-.]+", " ", value)
    value = re.sub(r"\s+", " ", value).strip()
    return value


# -----------------------------
# Classification rules
# -----------------------------

def image_type_tokens(row: dict) -> Set[str]:
    return {t.casefold() for t in get(row, "image_type").split("\\") if t}


def is_original(row: dict) -> bool:
    return "original" in image_type_tokens(row)


def is_derived(row: dict) -> bool:
    return "derived" in image_type_tokens(row)


def is_true_mpr_reformat(row: dict) -> bool:
    """
    Detect true MPR/reformat derivatives without accidentally excluding MPRAGE.

    Examples:
      Sag_T1_3D_MPRAGE_Wave          -> source T1w
      Sag_T1_3D_MPRAGE_Wave_MPR_Ax   -> derived reformat
      Sag_T1_3D_MPRAGE_Wave_MPR Cor  -> derived reformat
    """
    desc = get(row, "series_description")
    desc_norm = norm_text(desc)
    toks = image_type_tokens(row)

    explicit_mpr_suffix = bool(
        re.search(
            r"(^|[_\s-])MPR([_\s-]|$)|_MPR_| Ax MPR| Cor MPR| Tra MPR| Sag MPR",
            desc,
            re.IGNORECASE,
        )
    )

    if "mprage" in desc_norm or "memprage" in desc_norm:
        return explicit_mpr_suffix

    if explicit_mpr_suffix:
        return True

    if "mpr" in toks or "resampled" in toks:
        return True

    return False


def excluded_reason(row: dict) -> str:
    """
    Return empty string when row is eligible as source-like ORIGINAL.
    Otherwise return the reason it should be excluded from protocol discovery.
    """
    desc = get(row, "series_description")
    desc_norm = norm_text(desc)
    toks = image_type_tokens(row)

    if not is_original(row):
        return "not ORIGINAL image_type"

    if re.search(r"^(Loc|AAHScout|AAScout|Localizer|Scout|Survey)", desc, re.IGNORECASE):
        return "localizer/scout"

    if "mocoseries" in desc_norm:
        return "MoCo derivative"

    if re.search(r"PhoenixZIPReport|Visage\s+Presentation|DoseReport|Report", desc, re.IGNORECASE):
        return "scanner report"

    if is_true_mpr_reformat(row):
        return "MPR/reformat derivative"

    if re.search(r"(_ADC$|_FA$|_ColFA$|_TRACEW$|_TENSOR(?:_B0)?$)", desc, re.IGNORECASE):
        return "DWI derivative scalar/map"

    if any(t in toks for t in {"adc", "fa", "tracew", "tensor", "tensor_b0"}):
        return "DWI derivative image_type"

    if re.search(r"mIP|MNIP", desc, re.IGNORECASE):
        return "mIP/MNIP derivative"

    if re.search(r"PhysioLog|EvaSeries_GLM|Mean_&_t-Maps|Design$|relCBF|Perfusion_Weighted|WIP SOURCE", desc, re.IGNORECASE):
        return "known derivative/non-image output"

    if is_derived(row):
        return "DERIVED image_type"

    return ""


def canonical_sequence(row: dict) -> str:
    """
    Canonical protocol-comparison label. This is not necessarily final BIDS.
    """
    desc = get(row, "series_description")
    desc_norm = norm_text(desc)

    # T1w source families.
    if re.search(r"(MPRAGE|MEMPRAGE|Sag_T1|T1_3D|sT1W|T1w|BRAVO)", desc, re.IGNORECASE):
        if re.search(r"BRAVO|Pre Ax T1|Post Ax T1|FLASH", desc, re.IGNORECASE):
            return f"CLINICAL_OR_EXTRA:{desc}"
        return "T1w_MPRAGE_Wave" if "wave" in desc_norm else "T1w_MPRAGE"

    # Anatomical.
    if re.search(r"FLAIR", desc, re.IGNORECASE):
        return "FLAIR"
    if re.search(r"(^|[_\s])T2($|[_\s])|Ax_T2| T2", desc, re.IGNORECASE):
        return "T2w"

    # SWI-like. Kept as optional/reviewable protocol content, not auto-BIDS.
    if re.search(r"SWI", desc, re.IGNORECASE):
        if re.search(r"Mag", desc, re.IGNORECASE):
            return "SWI_Mag"
        if re.search(r"Pha", desc, re.IGNORECASE):
            return "SWI_Pha"
        return "SWI"

    # Fieldmaps by target group and direction. Collapse minor variant suffixes.
    if re.search(r"FieldMap|SpinEchoFieldMap", desc, re.IGNORECASE):
        target = "unknown"
        if re.search(r"Diff|DWI|DTI|dMRI", desc, re.IGNORECASE):
            target = "dwi"
        elif re.search(r"rsfMRI|REST|rfMRI", desc, re.IGNORECASE):
            target = "rest"
        elif re.search(r"fMRI|bold", desc, re.IGNORECASE):
            target = "task"

        direction = "UNK"
        if re.search(r"_AP($|_)|(^|[_\s])AP($|[_\s])", desc, re.IGNORECASE):
            direction = "AP"
        elif re.search(r"_PA($|_)|(^|[_\s])PA($|[_\s])", desc, re.IGNORECASE):
            direction = "PA"
        elif re.search(r"_LR($|_)|(^|[_\s])LR($|[_\s])", desc, re.IGNORECASE):
            direction = "LR"
        elif re.search(r"_RL($|_)|(^|[_\s])RL($|[_\s])", desc, re.IGNORECASE):
            direction = "RL"

        return f"FieldMap_{target}_{direction}"

    # DWI. Collapse small naming variants but preserve old/clinical vs modern research families.
    if re.search(r"(DWI|Diff|DTI|DIF|dMRI)", desc, re.IGNORECASE):
        if re.search(r"Ax[_\s]?DWI", desc, re.IGNORECASE):
            return "DWI_Ax_clinical_or_old"
        if re.search(r"1\.5mm|1p5", desc, re.IGNORECASE):
            return "DWI_3shell_1p5"
        if re.search(r"1p8|1\.8iso|32b0", desc, re.IGNORECASE):
            return "DWI_3shell_1p8"
        if re.search(r"2p0|2\.0iso", desc, re.IGNORECASE):
            return "DWI_3shell_2p0"
        if re.search(r"AP", desc, re.IGNORECASE):
            return "DWI_AP"
        if re.search(r"PA", desc, re.IGNORECASE):
            return "DWI_PA"
        if re.search(r"LR", desc, re.IGNORECASE):
            return "DWI_LR"
        if re.search(r"RL", desc, re.IGNORECASE):
            return "DWI_RL"
        return "DWI_other"

    # Functional tasks/rest.
    if re.search(r"(fMRI|rsfMRI|REST|LANGUAGE|TENNIS|MOCA|bold|rfMRI|EMOTION|HAND)", desc, re.IGNORECASE):
        upper = desc.upper()
        if "LANGUAGE" in upper:
            return "BOLD_task-language"
        if "TENNIS" in upper:
            return "BOLD_task-tennis"
        if "EMOTION" in upper:
            return "BOLD_task-emotion"
        if "HAND" in upper:
            return "BOLD_task-hand"
        if "REST" in upper or "RSFMRI" in upper or "RFMRI" in upper or "BOLD_M600" in upper:
            return "BOLD_task-rest"
        return "BOLD_other"

    # Known clinical/extra examples.
    if re.search(r"(FLASH|Pre Ax T1|Post Ax T1)", desc, re.IGNORECASE):
        return f"CLINICAL_OR_EXTRA:{desc}"

    return f"OTHER:{desc}"


def modality_group(seq: str) -> str:
    if seq.startswith("T1w"):
        return "anat_t1w"
    if seq == "FLAIR":
        return "anat_flair"
    if seq == "T2w":
        return "anat_t2w"
    if seq.startswith("SWI"):
        return "swi_optional"
    if seq.startswith("FieldMap"):
        return "fmap"
    if seq.startswith("DWI_3shell"):
        return "dwi_research"
    if seq.startswith("DWI_Ax"):
        return "dwi_old_or_clinical"
    if seq.startswith("DWI"):
        return "dwi"
    if seq.startswith("BOLD"):
        return "func"
    if seq.startswith("CLINICAL_OR_EXTRA"):
        return "clinical_extra"
    return "other"


def is_research_candidate(seq: str) -> bool:
    """
    Determines whether a kept source-like series can contribute to core protocol discovery.

    Rare clinical extras are still listed in the subject review, but they do not define
    protocol families by default.
    """
    if seq.startswith("CLINICAL_OR_EXTRA:") or seq.startswith("OTHER:"):
        return False
    return True


