
"""
review_series_protocols.py
==========================

Prevalence-based protocol review for per-series TSV inventories produced by
query_series.py.

This script is intentionally focused on protocol review, not BIDS conversion.

It writes four review TSVs:
  01_sequence_prevalence.tsv
  02_subject_protocol_review.tsv
  03_protocol_family_summary.tsv
  04_excluded_series_audit.tsv

Modes
-----
detect:
    Use a concatenated TSV containing many subjects/sessions to discover core
    research sequences and protocol families.

identify:
    Use an individual subject/session TSV and compare it to an existing
    protocol family summary generated by detect mode.

Examples
--------
Detect protocol families from a concatenated TSV:

    python review_series_protocols.py \
        --input all_ses-001_series.tsv \
        --output-dir review_COMPASS \
        --concat \
        --mode detect \
        --site COMPASS

Identify one subject against existing families:

    python review_series_protocols.py \
        --input sub-CC001_ses-001_series.tsv \
        --output-dir review_sub-CC001 \
        --mode identify \
        --protocol-summary review_COMPASS/03_protocol_family_summary.tsv

Notes
-----
- Keeps source-like ORIGINAL rows.
- Excludes derived rows, localizers/scouts, MoCoSeries, DWI scalar maps,
  MPR reformats, mIP/MNIP, PhoenixZIPReport, and other scanner reports.
- Protects MPRAGE/MEMPRAGE/Sag_T1_3D_MPRAGE_Wave as source T1w.
- Uses prevalence rather than exact sequence-set differences to avoid creating
  fake protocol variants from rare clinical/add-on scans.
"""

