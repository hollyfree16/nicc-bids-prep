def parse_seq_list(value: str) -> Set[str]:
    value = value or ""
    if not value.strip():
        return set()
    return {x.strip() for x in value.split(" + ") if x.strip()}


def load_protocol_summary(path: Path, core_threshold: float) -> List[dict]:
    rows, _ = read_tsv(path)
    threshold_col = f"core_sequences_{int(core_threshold * 100)}pct"

    for row in rows:
        if threshold_col not in row or not row.get(threshold_col):
            # Fall back to first core_sequences_*pct column.
            for key in row.keys():
                if key.startswith("core_sequences_") and key.endswith("pct") and key != "core_sequences_100pct":
                    row[threshold_col] = row.get(key, "")
                    break
        row["_core_set"] = parse_seq_list(row.get(threshold_col) or row.get("core_sequences_100pct", ""))
    return rows


def identify_subject(subject_row: dict, protocol_rows: Sequence[dict]) -> Tuple[dict, List[dict]]:
    subj_set = parse_seq_list(subject_row["research_sequence_set"])
    scored = []

    for proto in protocol_rows:
        core = proto.get("_core_set", set())
        if not core:
            score = 0.0
            missing = set()
            extra = subj_set
        else:
            score = len(subj_set & core) / len(core)
            missing = core - subj_set
            extra = subj_set - core

        scored.append({
            "protocol_family": proto.get("protocol_family", ""),
            "score": round(score, 3),
            "n_core_expected": len(core),
            "n_core_present": len(subj_set & core),
            "missing_core_sequences": " + ".join(sorted(missing)),
            "extra_research_sequences": " + ".join(sorted(extra)),
        })

    scored.sort(key=lambda r: (-float(r["score"]), r["protocol_family"]))
    best = scored[0] if scored else {
        "protocol_family": "NO_PROTOCOL_SUMMARY",
        "score": 0.0,
        "n_core_expected": 0,
        "n_core_present": 0,
        "missing_core_sequences": "",
        "extra_research_sequences": "",
    }

    return best, scored


def identify_mode(args) -> None:
    rows, _ = read_tsv(args.input)
    kept, excluded = prepare_rows(rows)
    grouped = group_by_subject(kept)
    subject_rows = build_subject_reviews(grouped)

    if not subject_rows:
        raise SystemExit("ERROR: no source/original rows found for identify mode")

    protocols = load_protocol_summary(args.protocol_summary, args.core_threshold)

    identified = []
    all_scores = []

    for subj in subject_rows:
        best, scored = identify_subject(subj, protocols)
        subj["protocol_family"] = best["protocol_family"]
        subj["best_protocol_score"] = best["score"]
        subj["missing_core_sequences"] = best["missing_core_sequences"]
        subj["extra_research_sequences_vs_best"] = best["extra_research_sequences"]
        identified.append(subj)
        for row in scored:
            all_scores.append({
                "subject": subj["subject"],
                "session": subj["session"],
                "scan_date": subj["scan_date"],
                **row,
            })

    # In identify mode, still write the same four files, but protocol summary is a match summary.
    prevalence = build_sequence_prevalence(subject_rows, kept, args.core_threshold)

    write_tsv(args.output_dir / "01_sequence_prevalence.tsv", prevalence, [
        "canonical_sequence",
        "modality_group",
        "n_subjects",
        "n_total_subjects",
        "prevalence_pct",
        "role",
        "first_seen",
        "last_seen",
        "subjects",
        "raw_series_descriptions",
    ])

    write_tsv(args.output_dir / "02_subject_protocol_review.tsv", identified, [
        "subject",
        "session",
        "scan_date",
        "protocol_family",
        "best_protocol_score",
        "missing_core_sequences",
        "extra_research_sequences_vs_best",
        "n_original_kept_rows",
        "n_research_candidate_sequences",
        "research_sequence_set",
        "noncore_extra_sequence_set",
        "all_kept_sequence_set",
        "ordered_kept_series",
    ])

    # For identify mode, 03 is the score table against existing protocol families.
    write_tsv(args.output_dir / "03_protocol_family_summary.tsv", all_scores, [
        "subject",
        "session",
        "scan_date",
        "protocol_family",
        "score",
        "n_core_expected",
        "n_core_present",
        "missing_core_sequences",
        "extra_research_sequences",
    ])

    write_tsv(args.output_dir / "04_excluded_series_audit.tsv", excluded, [
        "subject",
        "session",
        "scan_date",
        "series_number",
        "series_description",
        "protocol_name",
        "image_type",
        "n_files",
        "exclude_reason",
    ])

    readme = f"""# Protocol identify outputs

Generated by `review_series_protocols.py --mode identify`.

Protocol summary used: {args.protocol_summary}

Outputs:
- `01_sequence_prevalence.tsv`: sequence prevalence within the input file.
- `02_subject_protocol_review.tsv`: best matching protocol family per subject.
- `03_protocol_family_summary.tsv`: score table against each known protocol family.
- `04_excluded_series_audit.tsv`: excluded rows and reasons.
"""
    (args.output_dir / "README.md").write_text(readme, encoding="utf-8")

    print(f"[identify] input rows: {len(rows)}")
    print(f"[identify] subjects/sessions: {len(subject_rows)}")
    print(f"[identify] protocol families compared: {len(protocols)}")
    print(f"[identify] wrote: {args.output_dir}")


