def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Detect or identify prevalence-based MRI protocol families from series TSV inventories."
    )

    parser.add_argument("--input", required=True, type=Path, help="Input per-series TSV. Can be one subject or concatenated subjects.")
    parser.add_argument("--output-dir", required=True, type=Path, help="Directory for review TSV outputs.")
    parser.add_argument("--mode", choices=["detect", "identify"], default="detect", help="detect discovers protocol families; identify matches to an existing summary.")
    parser.add_argument("--concat", action="store_true", help="Input is a concatenated TSV. Used as an explicit reminder; behavior is inferred from subject/session columns.")
    parser.add_argument("--site", default="", help="Optional site/project prefix for detected family names, e.g. COMPASS.")
    parser.add_argument("--core-threshold", type=float, default=0.80, help="Prevalence threshold for core sequences within a family. Default: 0.80.")
    parser.add_argument("--protocol-summary", type=Path, default=None, help="Required for --mode identify: 03_protocol_family_summary.tsv from detect mode.")

    return parser


def main(argv=None) -> int:
    args = build_parser().parse_args(argv)
    args.output_dir.mkdir(parents=True, exist_ok=True)

    if not args.input.exists():
        print(f"ERROR: input file does not exist: {args.input}", file=sys.stderr)
        return 2

    if not (0 < args.core_threshold <= 1):
        print("ERROR: --core-threshold must be between 0 and 1", file=sys.stderr)
        return 2

    if args.mode == "identify":
        if args.protocol_summary is None:
            print("ERROR: --protocol-summary is required in identify mode", file=sys.stderr)
            return 2
        if not args.protocol_summary.exists():
            print(f"ERROR: protocol summary not found: {args.protocol_summary}", file=sys.stderr)
            return 2
        identify_mode(args)
    else:
        detect_mode(args)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
