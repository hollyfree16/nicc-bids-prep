def prepare_rows(rows: Sequence[dict]) -> Tuple[List[dict], List[dict]]:
    kept, excluded = [], []

    for row in rows:
        reason = excluded_reason(row)
        if reason:
            excluded.append({
                "subject": get(row, "subject"),
                "session": get(row, "session"),
                "scan_date": get(row, "scan_date"),
                "series_number": get(row, "series_number"),
                "series_description": get(row, "series_description"),
                "protocol_name": get(row, "protocol_name"),
                "image_type": get(row, "image_type"),
                "n_files": get(row, "n_files"),
                "exclude_reason": reason,
            })
            continue

        rr = dict(row)
        seq = canonical_sequence(row)
        rr["canonical_sequence"] = seq
        rr["modality_group"] = modality_group(seq)
        rr["research_candidate"] = "yes" if is_research_candidate(seq) else "no"
        kept.append(rr)

    return kept, excluded


def group_by_subject(rows: Sequence[dict]) -> Dict[Tuple[str, str], List[dict]]:
    grouped = defaultdict(list)
    for row in rows:
        grouped[(get(row, "subject"), get(row, "session"))].append(row)

    for key in grouped:
        grouped[key].sort(key=lambda r: (
            get(r, "scan_date"),
            as_int(get(r, "series_number")),
            get(r, "series_time"),
            get(r, "series_description"),
        ))

    return dict(grouped)


def build_subject_reviews(grouped: Dict[Tuple[str, str], List[dict]]) -> List[dict]:
    subject_rows = []

    for (subject, session), rows in sorted(grouped.items(), key=lambda kv: (
        get(kv[1][0], "scan_date") if kv[1] else "",
        kv[0][0],
        kv[0][1],
    )):
        research = sorted({r["canonical_sequence"] for r in rows if r["research_candidate"] == "yes"})
        all_kept = sorted({r["canonical_sequence"] for r in rows})
        optional_extra = sorted(set(all_kept) - set(research))

        subject_rows.append({
            "subject": subject,
            "session": session,
            "scan_date": get(rows[0], "scan_date") if rows else "",
            "n_original_kept_rows": len(rows),
            "n_research_candidate_sequences": len(research),
            "research_sequence_set": " + ".join(research),
            "noncore_extra_sequence_set": " + ".join(optional_extra),
            "all_kept_sequence_set": " + ".join(all_kept),
            "ordered_kept_series": " | ".join(
                f"{get(r, 'series_number')}:{get(r, 'series_description')}->{r['canonical_sequence']}"
                for r in rows
            ),
        })

    return subject_rows


def build_sequence_prevalence(subject_rows: Sequence[dict], kept_rows: Sequence[dict], core_threshold: float) -> List[dict]:
    total = len(subject_rows)
    seq_subjects = defaultdict(set)
    seq_dates = defaultdict(list)
    seq_raw = defaultdict(Counter)

    for subject_row in subject_rows:
        subject = subject_row["subject"]
        date = subject_row["scan_date"]
        seqs = subject_row["research_sequence_set"].split(" + ") if subject_row["research_sequence_set"] else []
        for seq in seqs:
            seq_subjects[seq].add(subject)
            if date:
                seq_dates[seq].append(date)

    for row in kept_rows:
        if row.get("research_candidate") == "yes":
            seq_raw[row["canonical_sequence"]][get(row, "series_description")] += 1

    output = []
    for seq, subjects in sorted(seq_subjects.items(), key=lambda kv: (-len(kv[1]), kv[0])):
        pct = 100.0 * len(subjects) / total if total else 0.0
        if pct >= core_threshold * 100:
            role = "core"
        elif pct >= 50:
            role = "common_noncore"
        elif pct >= 20:
            role = "variant_specific"
        else:
            role = "rare_optional_or_clinical"

        output.append({
            "canonical_sequence": seq,
            "modality_group": modality_group(seq),
            "n_subjects": len(subjects),
            "n_total_subjects": total,
            "prevalence_pct": round(pct, 1),
            "role": role,
            "first_seen": min(seq_dates[seq]) if seq_dates[seq] else "",
            "last_seen": max(seq_dates[seq]) if seq_dates[seq] else "",
            "subjects": " | ".join(sorted(subjects)),
            "raw_series_descriptions": " | ".join(f"{d} ({c})" for d, c in seq_raw[seq].most_common()),
        })

    return output


def assign_protocol_family(seq_set: str) -> str:
    """
    Conservative family assignment. This avoids creating a new variant for one-off extras.
    """
    seqs = set(seq_set.split(" + ")) if seq_set else set()

    if len(seqs) <= 3:
        return "INCOMPLETE_OR_OFFPROTOCOL"

    modern_fmaps = {
        "FieldMap_task_AP",
        "FieldMap_task_PA",
        "FieldMap_rest_AP",
        "FieldMap_rest_PA",
        "FieldMap_dwi_AP",
        "FieldMap_dwi_PA",
    }
    modern_tasks = {"BOLD_task-language", "BOLD_task-tennis", "BOLD_task-rest"}

    if modern_fmaps.issubset(seqs) and modern_tasks.issubset(seqs) and "DWI_3shell_1p8" in seqs:
        return "V2_MODERN_FIELDMAP_1P8"

    if "DWI_3shell_1p5" in seqs and "BOLD_task-rest" in seqs:
        return "V1_OLD_1P5"

    return "REVIEW_MIXED_OR_EXTRA"


def build_family_summary(subject_rows: Sequence[dict], core_threshold: float, site: str) -> Tuple[List[dict], List[dict]]:
    grouped = defaultdict(list)

    for row in subject_rows:
        family = assign_protocol_family(row["research_sequence_set"])
        if site:
            family = f"{site}_{family}"
        row["protocol_family"] = family
        grouped[family].append(row)

    family_rows, sequence_rows = [], []

    for family, members in sorted(grouped.items(), key=lambda kv: (
        min((m["scan_date"] for m in kv[1] if m["scan_date"]), default=""),
        kv[0],
    )):
        n = len(members)
        sets = [
            set(m["research_sequence_set"].split(" + ")) if m["research_sequence_set"] else set()
            for m in members
        ]
        union = sorted(set.union(*sets)) if sets else []
        counts = Counter()
        for seq_set in sets:
            counts.update(seq_set)

        core_100 = [seq for seq in union if counts[seq] == n]
        core_threshold_seqs = [seq for seq in union if n and counts[seq] / n >= core_threshold]
        noncore = [seq for seq in union if seq not in core_threshold_seqs]
        dates = sorted(m["scan_date"] for m in members if m["scan_date"])

        family_rows.append({
            "protocol_family": family,
            "n_subjects": n,
            "first_scan_date": dates[0] if dates else "",
            "last_scan_date": dates[-1] if dates else "",
            "subjects": " | ".join(f"{m['subject']}:{m['scan_date']}" for m in members),
            "core_sequences_100pct": " + ".join(core_100),
            f"core_sequences_{int(core_threshold * 100)}pct": " + ".join(core_threshold_seqs),
            "noncore_optional_or_extra_sequences": " + ".join(noncore),
        })

        for seq in union:
            sequence_rows.append({
                "protocol_family": family,
                "canonical_sequence": seq,
                "modality_group": modality_group(seq),
                "n_subjects_with_sequence": counts[seq],
                "n_subjects_in_family": n,
                "prevalence_pct": round(100.0 * counts[seq] / n, 1) if n else 0.0,
                "role_in_family": (
                    "core_100pct" if counts[seq] == n else
                    f"core_{int(core_threshold * 100)}pct" if n and counts[seq] / n >= core_threshold else
                    "noncore_optional_or_extra"
                ),
            })

    return family_rows, sequence_rows


def write_detect_outputs(
    output_dir: Path,
    kept_rows: Sequence[dict],
    excluded_rows: Sequence[dict],
    subject_rows: List[dict],
    prevalence_rows: Sequence[dict],
    family_rows: Sequence[dict],
    core_threshold: float,
) -> None:
    # 01
    write_tsv(output_dir / "01_sequence_prevalence.tsv", prevalence_rows, [
        "canonical_sequence",
        "modality_group",
        "n_subjects",
        "n_total_subjects",
        "prevalence_pct",
        "role",
        "first_seen",
        "last_seen",
        "subjects",
        "raw_series_descriptions",
    ])

    # 02
    write_tsv(output_dir / "02_subject_protocol_review.tsv", subject_rows, [
        "subject",
        "session",
        "scan_date",
        "protocol_family",
        "n_original_kept_rows",
        "n_research_candidate_sequences",
        "research_sequence_set",
        "noncore_extra_sequence_set",
        "all_kept_sequence_set",
        "ordered_kept_series",
    ])

    # 03
    threshold_col = f"core_sequences_{int(core_threshold * 100)}pct"
    write_tsv(output_dir / "03_protocol_family_summary.tsv", family_rows, [
        "protocol_family",
        "n_subjects",
        "first_scan_date",
        "last_scan_date",
        "subjects",
        "core_sequences_100pct",
        threshold_col,
        "noncore_optional_or_extra_sequences",
    ])

    # 04
    write_tsv(output_dir / "04_excluded_series_audit.tsv", excluded_rows, [
        "subject",
        "session",
        "scan_date",
        "series_number",
        "series_description",
        "protocol_name",
        "image_type",
        "n_files",
        "exclude_reason",
    ])

    # Small README
    readme = f"""# Protocol review outputs

Generated by `review_series_protocols.py`.

Default outputs:
- `01_sequence_prevalence.tsv`: sequence-level prevalence across subjects.
- `02_subject_protocol_review.tsv`: subject-level source sequence sets and assigned family.
- `03_protocol_family_summary.tsv`: human-readable protocol family summaries.
- `04_excluded_series_audit.tsv`: excluded rows and reasons.

Core threshold: {core_threshold:.2f}

Rows kept for protocol discovery: {len(kept_rows)}
Rows excluded: {len(excluded_rows)}
Subjects/sessions reviewed: {len(subject_rows)}
Protocol families: {len(family_rows)}
"""
    (output_dir / "README.md").write_text(readme, encoding="utf-8")


def detect_mode(args) -> None:
    rows, _ = read_tsv(args.input)
    kept, excluded = prepare_rows(rows)
    grouped = group_by_subject(kept)
    subject_rows = build_subject_reviews(grouped)
    prevalence_rows = build_sequence_prevalence(subject_rows, kept, args.core_threshold)
    family_rows, _family_sequence_rows = build_family_summary(subject_rows, args.core_threshold, args.site)

    write_detect_outputs(
        args.output_dir,
        kept,
        excluded,
        subject_rows,
        prevalence_rows,
        family_rows,
        args.core_threshold,
    )

    print(f"[detect] input rows: {len(rows)}")
    print(f"[detect] kept ORIGINAL/source rows: {len(kept)}")
    print(f"[detect] excluded rows: {len(excluded)}")
    print(f"[detect] subjects/sessions: {len(subject_rows)}")
    print(f"[detect] protocol families: {len(family_rows)}")
    print(f"[detect] wrote: {args.output_dir}")


